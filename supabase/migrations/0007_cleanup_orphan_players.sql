-- ============================================================
-- 0007 고아 마스터 선수 정리 (1회성 데이터 정리)
-- ============================================================
-- 0006 이전, 팀라벨 트리거 무한재귀(stack depth)로 등록이 실패할 때
-- 마스터 선수(players)만 먼저 생성되고 tournament_players 삽입은 실패해
-- 어느 대회에도 등록되지 않은 "고아" 선수 행이 남았다.
-- 이 때문에 새 선수를 등록해도 "같은 이름의 선수가 있습니다" 모달이 떴다.
--
-- players 는 tournament_players.player_id 로만 참조되므로,
-- 어떤 대회 참가에도 연결되지 않은 마스터 선수는 안전하게 삭제할 수 있다.
-- (이 앱에서 마스터 선수는 '선수 등록' 시에만 생성되므로 미참가 = 고아)

delete from players p
where not exists (
  select 1 from tournament_players tp where tp.player_id = p.id
);
